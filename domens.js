  // ── Показать/скрыть ──────────────────────────────────────
  function isVisible() { return _visible; }
  function setVisible(v) { _visible = !!v; }
  function toggle() { _visible = !_visible; return _visible; }

  // ── Отрисовка поверх canvas ──────────────────────────────
  // xyToPixel — функция из MapModule
  function draw(ctx, imgW, imgH) {
    if (!_visible || typeof MapModule === 'undefined') return;
    POLYGONS.forEach(function(domen) {
      if (!domen.pts || domen.pts.length < 3) return;
      ctx.save();
      ctx.beginPath();
      var first = MapModule.xyToPixel(domen.pts[0][0], domen.pts[0][1], imgW, imgH);
      ctx.moveTo(first.px, first.py);
      for (var i = 1; i < domen.pts.length; i++) {
        var p = MapModule.xyToPixel(domen.pts[i][0], domen.pts[i][1], imgW, imgH);
        ctx.lineTo(p.px, p.py);
      }
      ctx.closePath();
      ctx.fillStyle   = domen.fill;
      ctx.fill();
      ctx.strokeStyle = domen.stroke;
      ctx.lineWidth   = 1.5;
      ctx.setLineDash([]);
      ctx.stroke();
      // Подпись в центре полигона
      var cx = 0, cy = 0;
      domen.pts.forEach(function(pt) {
        var px = MapModule.xyToPixel(pt[0], pt[1], imgW, imgH);
        cx += px.px; cy += px.py;
      });
      cx /= domen.pts.length;
      cy /= domen.pts.length;
      ctx.fillStyle    = domen.stroke;
      ctx.font         = 'bold 13px sans-serif';
      ctx.textAlign    = 'center';
      ctx.textBaseline = 'middle';
      // Белая обводка текста
      ctx.strokeStyle  = 'rgba(255,255,255,0.85)';
      ctx.lineWidth    = 3;
      ctx.strokeText(domen.name, cx, cy);
      ctx.fillText(domen.name, cx, cy);
      ctx.restore();
    });
  }

  // ── Найти домен по координатам (point-in-polygon) ─────────
  function findDomenAt(x, y) {
    for (var d = 0; d < POLYGONS.length; d++) {
      if (pointInPolygon(x, y, POLYGONS[d].pts)) return POLYGONS[d].name;
    }
    return null;
  }

  function pointInPolygon(x, y, pts) {
    var inside = false;
    for (var i = 0, j = pts.length - 1; i < pts.length; j = i++) {
      var xi = pts[i][0], yi = pts[i][1];
      var xj = pts[j][0], yj = pts[j][1];
      if (((yi > y) !== (yj > y)) && (x < (xj - xi) * (y - yi) / (yj - yi) + xi)) {
        inside = !inside;
      }
    }
    return inside;
  }

  function getList() { return POLYGONS; }
  function setColors(colorMap) {
    if (!colorMap) return;
    POLYGONS.forEach(function(d) {
      if (!colorMap[d.name]) return;
      d.stroke = colorMap[d.name];
      d.fill = hexToRgba(d.stroke, 0.22);
    });
  }
  function getColors() {
    var out = {};
    POLYGONS.forEach(function(d) { out[d.name] = d.stroke; });
    return out;
  }

  function hexToRgba(hex, alpha) {
    var h = (hex || '').replace('#', '');
    if (h.length !== 6) return 'rgba(120,120,120,' + alpha + ')';
    var r = parseInt(h.slice(0,2), 16);
    var g = parseInt(h.slice(2,4), 16);
    var b = parseInt(h.slice(4,6), 16);
    return 'rgba(' + r + ',' + g + ',' + b + ',' + alpha + ')';
  }

